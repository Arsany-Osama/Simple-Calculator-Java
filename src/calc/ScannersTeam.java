     public void Operations()
     {
     switch (calc)
                 {
                         //addition
                  case 1: ans=num+Double.parseDouble(text.getText()); //convert string to double
                          text.setText(Double.toString(ans)); //convert doublr to string
                          break;
                         //sub.
                  case 2: ans=num-Double.parseDouble(text.getText());
                          text.setText(Double.toString(ans));
                          break;
                        //Mult.       
                  case 3: ans=num*Double.parseDouble(text.getText());
                          text.setText(Double.toString(ans));
                          break;
                        //Division        
                  case 4: ans=num/Double.parseDouble(text.getText());
                          text.setText(Double.toString(ans));
                          break;     
                          //mod
                  case 5: ans=num%Double.parseDouble(text.getText());
                          text.setText(Double.toString(ans));
                          break;
                          //factorial
                  case 6: for(int i=1;i<=num2;i++)
                             {
                                   ans2=ans2*i;
                             }
                            text.setText(Double.toString(ans2));
                            ans2=1;
                            break;
                  case 7: ans=Math.pow(num, Double.parseDouble(text.getText()));
                          text.setText(Double.toString(ans));
                          break;
                }
     }
    
    
